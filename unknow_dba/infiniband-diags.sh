ibhosts: É utilizado para listar os hosts conectados à rede InfiniBand
ibstat: Exibe o status do adaptador InfiniBand no host local.
ibping: Testa a conectividade entre dois dispositivos InfiniBand.
ibnetdiscover: Gera um mapa completo da topologia da rede InfiniBand.
ibdiagnet: Ferramenta avançada para diagnóstico e análise da rede InfiniBand.